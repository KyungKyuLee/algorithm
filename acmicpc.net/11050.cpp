#include <iostream>

using namespace std;

int main(void)
{
	int n, k, i, sum = 1;
	cin >> n >> k;
	i = k;
	while (i)
	{
		sum *= n;
		n--;
		i--;
	}
	while (k)
	{
		sum /= k;
		k--;
	}
	cout << sum;
}